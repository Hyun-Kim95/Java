package org.opentutorials.javatutorials.constant;

public class ConstantDemo {

	public static void main(String[] args) {
		double a = 2.2;			// 기본
		float b = 2.2F;			// F를 붙혀서 사용
		int c = 2147483647; 	// 기본(int의 최대치)
		long d = 2147483648L;	//L를 붙혀서 사용
		byte e = 100;			// 그냥 사용가능
		short f = 200;			// 그냥 사용가능
	}

}
